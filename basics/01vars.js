//Muestra por consola una variable primaria 
// Recordar que el código se ejecuta de arriba hacia abajo
// Variable numérica
// js no distingue tipos de números
let numero = 5;
console.log(numero);

// variable de tipo cadena
// Se debe usar un tipo de comilla para abrir y cerrar. 
let palabra = "Hola mundo";
console.log(palabra);

//variable booleana
// true & false son palabras reservadas 
let respuesta = true;
console.log(respuesta);

//Declara e inicia una constante
// Las constantes no pueden cambiar su valor a través del código del programa
const PI = 3.1415;
console.log(PI);

// Cambia el valor de la variable numero
numero = -3.15;
console.log(numero)

// Cambia el valor de la variable palabra
palabra = "Adiós";
console.log(palabra);